import turtle as t
import math
t.shape('turtle')

a = [ (10, 10), (10, -10), (0, 0), (15, 10), (1, 1), (15, 0),
	  (25, 0), (25, 10), (25, -10), (0, 0), (30, 0), (1, 1),
	  (40, 10), (40, -10), (0, 0), (45, -10), (1, 1), (45, 0),
	  (55, 10), (45, 10), (0, 0), (60, 10), (1, 1), (70, 10),
	  (70, -10), (60, -10), (60, 10), (0, 0), (75, 10), (1, 1),
	  (85, 10), (85, -10), (75, -10), (75, 10) ]

for i in range(34):
	if a[i][0] == 0:
		t.penup()
	elif a[i][0] == 1:
		t.pendown()
	else:
		t.goto(a[i][0], a[i][1])

